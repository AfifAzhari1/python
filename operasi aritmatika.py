a = 10
b =2


#operasi tambah + 
hasil =a+b
print(a,'+',b,'=',hasil)

#operasi pengurangan -
hasil =a-b
print(a,'-',b,'=',hasil)

#operasi perkalian*
hasil =a*b
print(a,'*',b,'=',hasil)

#operasi pembagian /
hasil =a/b
print(a,'/',b,'=',hasil)

#operasi modulus %
hasil=a%b
print(a,'%',b,'=',hasil)

#operasi eksponen (pangkat)**
hasil =a**b
print(a,'**',b,'=',hasil)

#operasi floor division //
hasil =a//b
print(a,'//',b,'=',hasil)

